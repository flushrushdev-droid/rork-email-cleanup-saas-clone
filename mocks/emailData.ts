export function calculateSavings(sender: SenderProfile): { time: number; space: number } {
  const avgTimePerEmail = 30;
  const time = Math.round((sender.totalEmails * (100 - sender.engagementRate) / 100) * avgTimePerEmail / 60);
  const space = Math.round((sender.totalSize * (100 - sender.engagementRate) / 100) / (1024 * 1024));
  
  return { time, space };
}

export function formatBytes(bytes: number): string {
  if (bytes === 0) return '0 B';
  const k = 1024;
  const sizes = ['B', 'KB', 'MB', 'GB'];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  return Math.round(bytes / Math.pow(k, i) * 100) / 100 + ' ' + sizes[i];
}

